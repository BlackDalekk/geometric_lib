import math


def area(r):
    """
    Сalculation area of circle

    Args:
        r: Radius

    Returns:
        float: Circle's area
    """
    return math.pi * r * r


def perimeter(r):
    """
    Сalculation area of circle

    Args:
        r: Radius

    Returns:
        float: Circle's perimeter
    """
    return 2 * math.pi * r
