def area(a: float, h: float) -> float:
    """
    Сalculation area of tiangle

    Args:
        a: First side
        h: Height

    Returns:
        float: Tiangle's area
    """
    return a * h / 2 

def perimeter(a: float, b: float, c: float) -> float:
    """
    Сalculation perimeter of tiangle

    Args:
        a: First side
        b: Second side
        c: Third side

    Returns:
        float: Tiangle's perimeter
    """
    return a + b + c 
